package org.example.ch8.loading.transitivePersistence;

import javax.persistence.MappedSuperclass;

@MappedSuperclass
public class Person {
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
